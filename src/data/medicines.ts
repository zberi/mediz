export const getMedicinesByCategory = (categoryId: string): Medicine[] => {
  return medicines.filter(med => med.category === categoryId);
};

export const searchMedicines = (query: string): Medicine[] => {
  const lowerQuery = query.toLowerCase();
  return medicines.filter(
    med =>
      med.name.toLowerCase().includes(lowerQuery) ||
      med.genericName.toLowerCase().includes(lowerQuery) ||
      med.description.toLowerCase().includes(lowerQuery) ||
      med.category.toLowerCase().includes(lowerQuery) ||
      med.manufacturer.toLowerCase().includes(lowerQuery)
  );
};

export const getMedicineById = (id: string): Medicine | undefined => {
  return medicines.find(med => med.id === id);
};

// Helper to get popular diabetes medicines
export const getDiabetesMedicines = (): Medicine[] => {
  return medicines.filter(med => med.category === 'diabetes');
};

// Helper to get popular general medicines
export const getGeneralMedicines = (): Medicine[] => {
  return medicines.filter(med => med.category === 'general');
};
